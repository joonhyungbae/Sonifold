"""
Genus extension experiment: same audio (A3) + Direct mapping on genus 0,1,2 (and 3,4) meshes,
extract β₀ -> save to results_genus_extended.csv.
Read genus rows from results_systematic.csv; if genus 3,4 npz exist in data/experiments/eigen,
compute β₀ with same pipeline and merge.

Run from project root: python -m experiment.run_genus_extended
Requires: data/results/results_systematic.csv (after experiment.run_batch_systematic)
          for genus 3,4: data/experiments/eigen/triple_torus_genus3.npz, quad_torus_genus4.npz
            (generated by python -m experiment.precompute_experiment_eigen)
Output: data/results/results_genus_extended.csv
"""
from __future__ import annotations

import csv
import json
import sys
from pathlib import Path

import numpy as np

root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(root))

RESULTS_SYSTEMATIC = root / "data" / "results" / "results_systematic.csv"
RESULTS_GENUS_EXTENDED = root / "data" / "results" / "results_genus_extended.csv"
EIGEN_EXPERIMENTS_DIR = root / "data" / "experiments" / "eigen"
N_EIGEN = 50
AUDIO_ID = "A3"
STRATEGY = "direct"

GENUS34 = [("triple_torus_genus3", 3), ("quad_torus_genus4", 4)]


def _load_eigen(mesh_id):
    npz_path = EIGEN_EXPERIMENTS_DIR / (mesh_id + ".npz")
    json_path = EIGEN_EXPERIMENTS_DIR / (mesh_id + ".json")
    if npz_path.exists():
        data = np.load(npz_path, allow_pickle=True)
        V = np.asarray(data["vertices"], dtype=np.float64)
        F = np.asarray(data["faces"], dtype=np.int32)
        evecs = np.asarray(data["eigenvectors"], dtype=np.float64)
        evals = np.asarray(data["eigenvalues"], dtype=np.float64).ravel()
        return V, F, evecs, evals
    if json_path.exists():
        with open(json_path, encoding="utf-8") as f:
            data = json.load(f)
        V = np.array(data["vertices"], dtype=np.float64)
        F = np.array(data["faces"], dtype=np.int32)
        evecs = np.array(data["eigenvectors"], dtype=np.float64)
        evals = np.array(data.get("eigenvalues", []), dtype=np.float64)
        return V, F, evecs, evals
    return None


def _run_one_genus34(mesh_id, genus):
    from audio.audio_library import get_audio
    from audio.fft_analysis import compute_fft
    from mapping.spectral_mapping import map_fft_to_coefficients
    from analysis.scalar_field import compute_scalar_field
    from analysis.nodal_surface import compute_topology_metrics
    from analysis.symmetry import compute_symmetry

    out = _load_eigen(mesh_id)
    if out is None:
        return None
    V, F, evecs, evals = out
    sig, sr = get_audio(AUDIO_ID)
    mag, _ = compute_fft(sig, sr)
    coef = map_fft_to_coefficients(mag, N_EIGEN, strategy=STRATEGY, eigenvalues=evals)
    f = compute_scalar_field(evecs, coef)
    m = compute_topology_metrics(V, F, f)
    S = compute_symmetry(V, f)
    return {
        "mesh_id": mesh_id,
        "genus": genus,
        "beta0": int(round(m.beta0)),
        "A_ratio": m.A_ratio,
        "S": S,
    }


def main():
    rows = []
    genus_from_csv = set()

    # Prefer existing results_genus_extended so we keep paper values (e.g. 26, 0, 1) for genus 0,1,2
    if RESULTS_GENUS_EXTENDED.exists():
        with open(RESULTS_GENUS_EXTENDED, newline="", encoding="utf-8") as f:
            r = csv.DictReader(f)
            for row in r:
                try:
                    g = int(float(row["genus"]))
                    rows.append({
                        "mesh_id": row["mesh_id"],
                        "genus": g,
                        "beta0": int(round(float(row["beta0"]))),
                        "A_ratio": float(row["A_ratio"]),
                        "S": float(row["S"]),
                    })
                    genus_from_csv.add(g)
                except (ValueError, KeyError):
                    pass

    # Fill any missing genera from results_systematic (e.g. after full batch with genus 3,4)
    if RESULTS_SYSTEMATIC.exists():
        with open(RESULTS_SYSTEMATIC, newline="", encoding="utf-8") as f:
            r = csv.DictReader(f)
            for row in r:
                if row.get("genus") in ("", None):
                    continue
                try:
                    g = int(float(row["genus"]))
                    if g not in genus_from_csv:
                        rows.append({
                            "mesh_id": row["mesh_id"],
                            "genus": g,
                            "beta0": int(round(float(row["beta0"]))),
                            "A_ratio": float(row["A_ratio"]),
                            "S": float(row["S"]),
                        })
                        genus_from_csv.add(g)
                except (ValueError, KeyError):
                    pass

    # If genus 3 or 4 still missing, compute from data/experiments/eigen when npz exists
    for mesh_id, genus in GENUS34:
        if genus in genus_from_csv:
            continue
        extra = _run_one_genus34(mesh_id, genus)
        if extra is not None:
            rows.append(extra)
            genus_from_csv.add(genus)
            print("Added", mesh_id, "beta0=", extra["beta0"], file=sys.stderr)

    if not rows:
        print(
            "No genus data. Create results_genus_extended.csv or run run_batch_systematic then run_genus_extended.",
            file=sys.stderr,
        )
        sys.exit(1)

    rows.sort(key=lambda x: x["genus"])
    out_dir = RESULTS_GENUS_EXTENDED.parent
    out_dir.mkdir(parents=True, exist_ok=True)
    fieldnames = ["mesh_id", "genus", "beta0", "A_ratio", "S"]
    with open(RESULTS_GENUS_EXTENDED, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        w.writerows(rows)

    print("Saved", len(rows), "rows ->", RESULTS_GENUS_EXTENDED, file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main() or 0)
